    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String livroEscolhido = JOptionPane.showInputDialog("Informe o livro: ");
        Livro l = gerenciadorLivro.buscarLivro(livroEscolhido);
        
        if(l == null){
            JOptionPane.showMessageDialog(this, "nao tem");
            this.editando = false;
        } else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.objParaCampos(l);
            this.editando = true;
            this.tituloAntigo = l.getTitulo();
        }
    }//GEN-LAST:event_btnEditActionPerformed

