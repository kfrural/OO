    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String livroEscolhido = JOptionPane.showInputDialog("informe o livro pra excluir");
       Livro l = gerenciadorLivro.buscarLivro(livroEscolhido);
       
       if(l == null){
           JOptionPane.showMessageDialog(this, "nao tem");
       } else {
           gerenciadorLivro.removerLivro(livroEscolhido);
           JOptionPane.showMessageDialog(this, "removido");
       }
       
       String listagem = gerenciadorLivro.toString();
       edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Livro l = this.camposParaObj();
        if(this.editando){
            gerenciadorLivro.atualizarLivro(tituloAntigo, l);
        } else {
            gerenciadorLivro.addLivro(l);
        }
        
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;
        
        String listagem = gerenciadorLivro.toString();
        edtListagem.setText(listagem);
        
        gerenciadorLivro.salvarNoArquivo("ListagemLivro.csv");
    }//GEN-LAST:event_btnSalvarActionPerformed
