    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        String idEscolhido = edtId.getText();        
        if (!idEscolhido.isEmpty()) {
            livroEscolhido = this.gerenteLivros.buscarLivro(idEscolhido);

            if (livroEscolhido == null) {
                JOptionPane.showMessageDialog(this, "NÃ£o existe este ID.");
                lblLivroSelecionado.setText("");
            } else {
                lblLivroSelecionado.setText(livroEscolhido.toString());
            }
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAddLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLivroActionPerformed
        if (livroEscolhido == null) {
            int response = JOptionPane.showConfirmDialog(null,
                    "Nenhum produto foi escolhido. Deseja continuar?",
                    "Confirm",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.OK_OPTION) {
                dispose();
            }
        } else {
            this.avaliacao.addLivro(livroEscolhido);
            dispose();
        }
    }//GEN-LAST:event_btnAddLivroActionPerformed

